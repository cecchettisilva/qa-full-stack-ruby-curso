# Tipos de dados em Ruby:
# -> Tipo de uma variavel é definido no momento que a mesma recebe o valor
# Para saber o tipo da variável, basta colocar "nomeVariavel.class"

produto = 'TV'
preco = 10000.00
quantidade = 10
disponivel = true 
 
puts produto.class # String
puts preco.class # Float
puts quantidade.class # Integer
puts disponivel.class # TrueClass

puts produto #imprime o valor
puts preco #imprime o valor
puts quantidade #imprime o valor
puts disponivel #imprime o valor



